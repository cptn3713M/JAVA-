public class swappingwithvariable {
    public static void main(String[] args)
    {
        int a=28,b=43;
        int c;
        System.out.println("before swapping=\n"+a +b);
        System.out.println(b);
       // System.out.println(a+ +b);
       c=a;
       a=b;
       b=c;
        System.out.println("afterswap\n"+a +b);
        System.out.println(b);}
}

